#include "ft_strrev.h"

void	ft_strrev(char *str)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	while (str[i])
		i++;
	j = 0;
	i--;
	while (i > j)
	{
		c = str[i];
		str[i] = str[j];
		str[j] = c;
		i--;
		j++;	
	}
}
